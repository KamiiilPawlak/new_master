""" Docstring for 1.convert.distance """

def m_to_cm(values):
    return values * 100


def cm_to_m(values):
    return values / 100


def cm_to_mm(values):
    return values * 10


def mm_to_cm(values):
    return values / 10


def m_to_mm(values):
    return values * 1000


def mm_to_m(values):
    return values / 1000

